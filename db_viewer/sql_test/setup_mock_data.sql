/
BEGIN EXECUTE IMMEDIATE 'DROP SEQUENCE SEQ_CATEGORIES'; EXCEPTION WHEN OTHERS THEN IF SQLCODE != -2289 THEN RAISE; END IF; END;
/

-- ===================== SEQUENCES =============================================
CREATE SEQUENCE SEQ_CATEGORIES START WITH 1 INCREMENT BY 1 NOCACHE;
CREATE SEQUENCE SEQ_CUSTOMERS  START WITH 1 INCREMENT BY 1 NOCACHE;
CREATE SEQUENCE SEQ_PRODUCTS   START WITH 1 INCREMENT BY 1 NOCACHE;
CREATE SEQUENCE SEQ_ORDERS     START WITH 1 INCREMENT BY 1 NOCACHE;
CREATE SEQUENCE SEQ_EMPLOYEES  START WITH 1 INCREMENT BY 1 NOCACHE;
CREATE SEQUENCE SEQ_AUDIT_LOG  START WITH 1 INCREMENT BY 1 NOCACHE;

-- ===================== TABLE: CATEGORIES =====================================
CREATE TABLE CATEGORIES (
    CATEGORY_ID   NUMBER        DEFAULT SEQ_CATEGORIES.NEXTVAL PRIMARY KEY,
    CATEGORY_NAME VARCHAR2(100) NOT NULL,
    DESCRIPTION   VARCHAR2(500),
    IS_ACTIVE     CHAR(1)       DEFAULT 'Y' CHECK (IS_ACTIVE IN ('Y','N')),
    CREATED_AT    TIMESTAMP     DEFAULT SYSTIMESTAMP
);

CREATE INDEX IDX_CATEGORIES_NAME ON CATEGORIES(CATEGORY_NAME);

-- ===================== TABLE: CUSTOMERS ======================================
CREATE TABLE CUSTOMERS (
    CUSTOMER_ID   NUMBER         DEFAULT SEQ_CUSTOMERS.NEXTVAL PRIMARY KEY,
    FIRST_NAME    VARCHAR2(50)   NOT NULL,
    LAST_NAME     VARCHAR2(50)   NOT NULL,
    EMAIL         VARCHAR2(120)  NOT NULL UNIQUE,
    PHONE         VARCHAR2(30),
    ADDRESS       VARCHAR2(200),
    CITY          VARCHAR2(80),
    COUNTRY       VARCHAR2(60),
    CREDIT_LIMIT  NUMBER(12,2)   DEFAULT 0,
    MEMBERSHIP    VARCHAR2(20)   DEFAULT 'STANDARD' CHECK (MEMBERSHIP IN ('STANDARD','SILVER','GOLD','PLATINUM')),
    DATE_OF_BIRTH DATE,
    REGISTERED_AT TIMESTAMP      DEFAULT SYSTIMESTAMP,
    NOTES         CLOB
);

CREATE INDEX IDX_CUSTOMERS_NAME    ON CUSTOMERS(LAST_NAME, FIRST_NAME);
CREATE INDEX IDX_CUSTOMERS_COUNTRY ON CUSTOMERS(COUNTRY);
CREATE INDEX IDX_CUSTOMERS_MEMBER  ON CUSTOMERS(MEMBERSHIP);

-- ===================== TABLE: PRODUCTS =======================================
CREATE TABLE PRODUCTS (
    PRODUCT_ID   NUMBER         DEFAULT SEQ_PRODUCTS.NEXTVAL PRIMARY KEY,
    PRODUCT_NAME VARCHAR2(150)  NOT NULL,
    CATEGORY_ID  NUMBER         REFERENCES CATEGORIES(CATEGORY_ID),
    SKU          VARCHAR2(30)   NOT NULL UNIQUE,
    PRICE        NUMBER(10,2)   NOT NULL CHECK (PRICE >= 0),
    STOCK_QTY    NUMBER         DEFAULT 0,
    WEIGHT_KG    NUMBER(8,3),
    IS_AVAILABLE CHAR(1)        DEFAULT 'Y' CHECK (IS_AVAILABLE IN ('Y','N')),
    DESCRIPTION  VARCHAR2(1000),
    CREATED_AT   TIMESTAMP      DEFAULT SYSTIMESTAMP,
    UPDATED_AT   TIMESTAMP      DEFAULT SYSTIMESTAMP
);

CREATE INDEX IDX_PRODUCTS_CATEGORY ON PRODUCTS(CATEGORY_ID);
CREATE INDEX IDX_PRODUCTS_SKU      ON PRODUCTS(SKU);
CREATE INDEX IDX_PRODUCTS_PRICE    ON PRODUCTS(PRICE);

-- ===================== TABLE: ORDERS =========================================
CREATE TABLE ORDERS (
    ORDER_ID      NUMBER         DEFAULT SEQ_ORDERS.NEXTVAL PRIMARY KEY,
    CUSTOMER_ID   NUMBER         NOT NULL REFERENCES CUSTOMERS(CUSTOMER_ID),
    PRODUCT_ID    NUMBER         NOT NULL REFERENCES PRODUCTS(PRODUCT_ID),
    QUANTITY      NUMBER         NOT NULL CHECK (QUANTITY > 0),
    UNIT_PRICE    NUMBER(10,2)   NOT NULL,
    TOTAL_AMOUNT  NUMBER(12,2)   GENERATED ALWAYS AS (QUANTITY * UNIT_PRICE) VIRTUAL,
    STATUS        VARCHAR2(20)   DEFAULT 'PENDING' CHECK (STATUS IN ('PENDING','CONFIRMED','SHIPPED','DELIVERED','CANCELLED')),
    ORDER_DATE    DATE           DEFAULT SYSDATE,
    SHIPPED_DATE  DATE,
    DELIVERY_NOTE VARCHAR2(500),
    CREATED_AT    TIMESTAMP      DEFAULT SYSTIMESTAMP
);

CREATE INDEX IDX_ORDERS_CUSTOMER ON ORDERS(CUSTOMER_ID);
CREATE INDEX IDX_ORDERS_PRODUCT  ON ORDERS(PRODUCT_ID);
CREATE INDEX IDX_ORDERS_STATUS   ON ORDERS(STATUS);
CREATE INDEX IDX_ORDERS_DATE     ON ORDERS(ORDER_DATE);

-- ===================== TABLE: EMPLOYEES ======================================
CREATE TABLE EMPLOYEES (
    EMPLOYEE_ID   NUMBER         DEFAULT SEQ_EMPLOYEES.NEXTVAL PRIMARY KEY,
    FIRST_NAME    VARCHAR2(50)   NOT NULL,
    LAST_NAME     VARCHAR2(50)   NOT NULL,
    EMAIL         VARCHAR2(120)  NOT NULL UNIQUE,
    PHONE         VARCHAR2(30),
    DEPARTMENT    VARCHAR2(60),
    JOB_TITLE     VARCHAR2(80),
    SALARY        NUMBER(12,2)   CHECK (SALARY >= 0),
    HIRE_DATE     DATE           DEFAULT SYSDATE,
    MANAGER_ID    NUMBER         REFERENCES EMPLOYEES(EMPLOYEE_ID),
    IS_ACTIVE     CHAR(1)        DEFAULT 'Y' CHECK (IS_ACTIVE IN ('Y','N')),
    CREATED_AT    TIMESTAMP      DEFAULT SYSTIMESTAMP
);

CREATE INDEX IDX_EMPLOYEES_DEPT    ON EMPLOYEES(DEPARTMENT);
CREATE INDEX IDX_EMPLOYEES_MANAGER ON EMPLOYEES(MANAGER_ID);
CREATE INDEX IDX_EMPLOYEES_NAME    ON EMPLOYEES(LAST_NAME, FIRST_NAME);

-- ===================== TABLE: AUDIT_LOG ======================================
CREATE TABLE AUDIT_LOG (
    LOG_ID        NUMBER         DEFAULT SEQ_AUDIT_LOG.NEXTVAL PRIMARY KEY,
    TABLE_NAME    VARCHAR2(60)   NOT NULL,
    OPERATION     VARCHAR2(10)   NOT NULL CHECK (OPERATION IN ('INSERT','UPDATE','DELETE')),
    RECORD_ID     NUMBER,
    OLD_VALUE     CLOB,
    NEW_VALUE     CLOB,
    CHANGED_BY    VARCHAR2(60),
    CHANGED_AT    TIMESTAMP      DEFAULT SYSTIMESTAMP
);

CREATE INDEX IDX_AUDIT_TABLE ON AUDIT_LOG(TABLE_NAME);
CREATE INDEX IDX_AUDIT_OP    ON AUDIT_LOG(OPERATION);
CREATE INDEX IDX_AUDIT_DATE  ON AUDIT_LOG(CHANGED_AT);

